# آلة حاسبة بسيطة - مشروع Python

def جمع(أ, ب):
    """جمع عددين"""
    return أ + ب

def طرح(أ, ب):
    """طرح عددين"""
    return أ - ب

def ضرب(أ, ب):
    """ضرب عددين"""
    return أ * ب

def قسمة(أ, ب):
    """قسمة عددين"""
    if ب == 0:
        raise ValueError("لا يمكن القسمة على صفر!")
    return أ / ب

def حاسبة():
    """الدالة الرئيسية للآلة الحاسبة"""
    print("=== آلة حاسبة بسيطة ===")
    print("العمليات المتاحة:")
    print("1. جمع (+)")
    print("2. طرح (-)")
    print("3. ضرب (*)")
    print("4. قسمة (/)")
    print("5. خروج")

    while True:
        اختيار = input("\nاختر العملية (1-5): ").strip()

        if اختيار == "5":
            print("وداعاً!")
            break

        if اختيار not in ["1", "2", "3", "4"]:
            print("اختيار غير صحيح، حاول مرة أخرى")
            continue

        try:
            الرقم1 = float(input("أدخل الرقم الأول: "))
            الرقم2 = float(input("أدخل الرقم الثاني: "))
        except ValueError:
            print("خطأ: يرجى إدخال أرقام صحيحة")
            continue

        try:
            if اختيار == "1":
                print(f"النتيجة: {جمع(الرقم1, الرقم2)}")
            elif اختيار == "2":
                print(f"النتيجة: {طرح(الرقم1, الرقم2)}")
            elif اختيار == "3":
                print(f"النتيجة: {ضرب(الرقم1, الرقم2)}")
            elif اختيار == "4":
                print(f"النتيجة: {قسمة(الرقم1, الرقم2)}")
        except ValueError as خطأ:
            print(f"خطأ: {خطأ}")


if __name__ == "__main__":
    حاسبة()

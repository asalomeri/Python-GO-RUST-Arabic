# تطبيق قائمة المهام - Todo App

import json
import os
from datetime import datetime

ملف_المهام = "tasks.json"

def تحميل_المهام():
    """تحميل المهام من الملف"""
    if os.path.exists(ملف_المهام):
        with open(ملف_المهام, "r", encoding="utf-8") as ملف:
            return json.load(ملف)
    return []

def حفظ_المهام(مهام):
    """حفظ المهام في الملف"""
    with open(ملف_المهام, "w", encoding="utf-8") as ملف:
        json.dump(مهام, ملف, ensure_ascii=False, indent=2)

def إضافة_مهمة(مهام, عنوان):
    """إضافة مهمة جديدة"""
    مهمة = {
        "id": len(مهام) + 1,
        "عنوان": عنوان,
        "مكتملة": False,
        "تاريخ_الإنشاء": datetime.now().isoformat()
    }
    مهام.append(مهمة)
    حفظ_المهام(مهام)
    print(f"✅ تم إضافة المهمة: {عنوان}")

def عرض_المهام(مهام):
    """عرض جميع المهام"""
    if not مهام:
        print("لا توجد مهام بعد!")
        return

    print("\n=== قائمة المهام ===")
    for مهمة in مهام:
        حالة = "✅" if مهمة["مكتملة"] else "⬜"
        print(f"{حالة} [{مهمة['id']}] {مهمة['عنوان']}")

def إكمال_مهمة(مهام, رقم_المهمة):
    """تحديد مهمة كمكتملة"""
    for مهمة in مهام:
        if مهمة["id"] == رقم_المهمة:
            مهمة["مكتملة"] = True
            حفظ_المهام(مهام)
            print(f"✅ تم إكمال المهمة: {مهمة['عنوان']}")
            return
    print(f"❌ لم يتم العثور على مهمة برقم {رقم_المهمة}")

def حذف_مهمة(مهام, رقم_المهمة):
    """حذف مهمة"""
    for مهمة in مهام:
        if مهمة["id"] == رقم_المهمة:
            مهام.remove(مهمة)
            حفظ_المهام(مهام)
            print(f"🗑️ تم حذف المهمة: {مهمة['عنوان']}")
            return
    print(f"❌ لم يتم العثور على مهمة برقم {رقم_المهمة}")

def main():
    مهام = تحميل_المهام()

    while True:
        print("\n=== تطبيق قائمة المهام ===")
        print("1. عرض المهام")
        print("2. إضافة مهمة")
        print("3. إكمال مهمة")
        print("4. حذف مهمة")
        print("5. خروج")

        اختيار = input("\nاختر: ").strip()

        if اختيار == "1":
            عرض_المهام(مهام)
        elif اختيار == "2":
            عنوان = input("عنوان المهمة: ").strip()
            if عنوان:
                إضافة_مهمة(مهام, عنوان)
        elif اختيار == "3":
            عرض_المهام(مهام)
            try:
                رقم = int(input("رقم المهمة: "))
                إكمال_مهمة(مهام, رقم)
            except ValueError:
                print("خطأ: أدخل رقماً صحيحاً")
        elif اختيار == "4":
            عرض_المهام(مهام)
            try:
                رقم = int(input("رقم المهمة للحذف: "))
                حذف_مهمة(مهام, رقم)
            except ValueError:
                print("خطأ: أدخل رقماً صحيحاً")
        elif اختيار == "5":
            print("وداعاً! 👋")
            break
        else:
            print("اختيار غير صحيح")


if __name__ == "__main__":
    main()

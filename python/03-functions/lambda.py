# دوال Lambda في بايثون

# ===== lambda بسيطة =====
مربع = lambda x: x ** 2
print(f"مربع 5: {مربع(5)}")

# ===== lambda بمعاملين =====
جمع = lambda أ, ب: أ + ب
print(f"3 + 4 = {جمع(3, 4)}")

# ===== lambda مع map =====
أعداد = [1, 2, 3, 4, 5]
مربعات = list(map(lambda x: x**2, أعداد))
print(f"المربعات: {مربعات}")

# ===== lambda مع filter =====
أعداد_زوجية = list(filter(lambda x: x % 2 == 0, أعداد))
print(f"الأعداد الزوجية: {أعداد_زوجية}")

# ===== lambda مع sorted =====
طلاب = [
    {"اسم": "أحمد", "درجة": 85},
    {"اسم": "محمد", "درجة": 92},
    {"اسم": "علي", "درجة": 78},
]
طلاب_مرتبون = sorted(طلاب, key=lambda طالب: طالب["درجة"], reverse=True)
for طالب in طلاب_مرتبون:
    print(f"{طالب['اسم']}: {طالب['درجة']}")

# ===== lambda مع reduce =====
from functools import reduce
حاصل_ضرب = reduce(lambda أ, ب: أ * ب, [1, 2, 3, 4, 5])
print(f"حاصل ضرب 1 إلى 5: {حاصل_ضرب}")

# الدوال في بايثون - Functions

# ===== دالة بسيطة =====
def مرحبا():
    print("مرحبا بالعالم!")

مرحبا()

# ===== دالة بمعاملات =====
def تحية(الاسم):
    print(f"مرحبا يا {الاسم}!")

تحية("أحمد")

# ===== دالة بقيمة افتراضية =====
def تحية_مع_لقب(الاسم, اللقب="السيد"):
    print(f"مرحبا يا {اللقب} {الاسم}!")

تحية_مع_لقب("محمد")
تحية_مع_لقب("سارة", "السيدة")

# ===== دالة بعودة =====
def جمع(أ, ب):
    return أ + ب

النتيجة = جمع(5, 3)
print(f"5 + 3 = {النتيجة}")

# ===== دالة تعيد عدة قيم =====
def عمليات_حسابية(أ, ب):
    return أ + ب, أ - ب, أ * ب, أ / ب

مجموع, فرق, حاصل, قسمة = عمليات_حسابية(10, 2)
print(f"المجموع: {مجموع}, الفرق: {فرق}, الحاصل: {حاصل}, القسمة: {قسمة}")

# ===== *args - عدد متغير من المعاملات =====
def جمع_أعداد(*أعداد):
    return sum(أعداد)

print(جمع_أعداد(1, 2, 3, 4, 5))

# ===== **kwargs - معاملات مسماة =====
def معلومات_شخص(**معلومات):
    for مفتاح, قيمة in معلومات.items():
        print(f"{مفتاح}: {قيمة}")

معلومات_شخص(اسم="علي", عمر=25, مدينة="جدة")

# ===== دوال متداخلة =====
def الدالة_الخارجية():
    رسالة = "مرحبا من الخارج"

    def الدالة_الداخلية():
        print(رسالة)

    الدالة_الداخلية()

الدالة_الخارجية()

# ===== الدوال كمعاملات =====
def تطبيق_عملية(أعداد, عملية):
    return [عملية(عدد) for عدد in أعداد]

أعداد = [1, 2, 3, 4, 5]
مربعات = تطبيق_عملية(أعداد, lambda x: x**2)
print(f"المربعات: {مربعات}")

# أنواع البيانات في بايثون - Data Types

# ===== النصوص (Strings) =====
نص1 = "مرحبا"
نص2 = 'بالعالم'
نص_متعدد = """هذا نص
على عدة أسطر"""

print("=== النصوص ===")
print(نص1 + " " + نص2)
print(len(نص1))          # طول النص
print(نص1.upper())       # أحرف كبيرة
print(نص1.lower())       # أحرف صغيرة
print(نص1[0])            # أول حرف

# ===== الأعداد الصحيحة (Integers) =====
print("\n=== الأعداد الصحيحة ===")
عدد1 = 10
عدد2 = 3
print(f"الجمع: {عدد1 + عدد2}")
print(f"الطرح: {عدد1 - عدد2}")
print(f"الضرب: {عدد1 * عدد2}")
print(f"القسمة: {عدد1 / عدد2}")
print(f"القسمة الصحيحة: {عدد1 // عدد2}")
print(f"باقي القسمة: {عدد1 % عدد2}")
print(f"الأس: {عدد1 ** عدد2}")

# ===== القوائم (Lists) =====
print("\n=== القوائم ===")
فواكه = ["تفاح", "موز", "برتقال", "عنب"]
print(فواكه)
print(فواكه[0])           # أول عنصر
print(فواكه[-1])          # آخر عنصر
فواكه.append("مانجو")    # إضافة عنصر
print(فواكه)
فواكه.remove("موز")      # حذف عنصر
print(فواكه)

# ===== القواميس (Dictionaries) =====
print("\n=== القواميس ===")
شخص = {
    "اسم": "محمد",
    "عمر": 30,
    "مدينة": "الرياض"
}
print(شخص["اسم"])
print(شخص.get("عمر"))
شخص["وظيفة"] = "مبرمج"
print(شخص)

# ===== المجموعات (Sets) =====
print("\n=== المجموعات ===")
أعداد = {1, 2, 3, 4, 5, 3, 2}  # بدون تكرار
print(أعداد)

# ===== الصفوف (Tuples) =====
print("\n=== الصفوف ===")
إحداثيات = (10, 20)
print(إحداثيات)
print(إحداثيات[0])

# الشروط في بايثون - If/Else

# ===== الشرط البسيط =====
درجة = 75

if درجة >= 90:
    print("ممتاز!")
elif درجة >= 80:
    print("جيد جداً")
elif درجة >= 70:
    print("جيد")
elif درجة >= 60:
    print("مقبول")
else:
    print("راسب")

# ===== الشرط الثلاثي (Ternary) =====
عمر = 20
الحالة = "بالغ" if عمر >= 18 else "قاصر"
print(f"الحالة: {الحالة}")

# ===== الشروط المركبة =====
درجة_حرارة = 25
طقس_ممطر = False

if درجة_حرارة > 20 and not طقس_ممطر:
    print("الطقس مناسب للنزهة!")
elif درجة_حرارة > 20 and طقس_ممطر:
    print("دافئ لكن ممطر")
else:
    print("الجو بارد")

# ===== التحقق من القيم =====
قائمة = [1, 2, 3, 4, 5]
if 3 in قائمة:
    print("العدد 3 موجود في القائمة")

اسم = None
if اسم is None:
    print("الاسم غير محدد")

# ===== match (Python 3.10+) =====
يوم = "الإثنين"
match يوم:
    case "السبت" | "الأحد":
        print("عطلة نهاية الأسبوع!")
    case "الإثنين":
        print("بداية أسبوع عمل جديد")
    case _:
        print("يوم عمل عادي")

# الوراثة في بايثون - Inheritance

# ===== الكلاس الأساسي =====
class مركبة:
    def __init__(self, الماركة, الموديل, السنة):
        self.الماركة = الماركة
        self.الموديل = الموديل
        self.السنة = السنة
        self.تشغيل = False

    def تشغيل_المحرك(self):
        self.تشغيل = True
        print(f"تم تشغيل محرك {self.الماركة} {self.الموديل}")

    def إيقاف_المحرك(self):
        self.تشغيل = False
        print(f"تم إيقاف محرك {self.الماركة} {self.الموديل}")

    def __str__(self):
        return f"{self.السنة} {self.الماركة} {self.الموديل}"


# ===== الوراثة البسيطة =====
class سيارة(مركبة):
    def __init__(self, الماركة, الموديل, السنة, عدد_الأبواب):
        super().__init__(الماركة, الموديل, السنة)
        self.عدد_الأبواب = عدد_الأبواب

    def فتح_صندوق(self):
        print(f"تم فتح صندوق {self.الماركة}")


class شاحنة(مركبة):
    def __init__(self, الماركة, الموديل, السنة, الحمولة_القصوى):
        super().__init__(الماركة, الموديل, السنة)
        self.الحمولة_القصوى = الحمولة_القصوى

    def تحميل_البضاعة(self, وزن):
        if وزن <= self.الحمولة_القصوى:
            print(f"تم تحميل {وزن} كغ بنجاح")
        else:
            print(f"الوزن {وزن} كغ يتجاوز الحمولة القصوى {self.الحمولة_القصوى} كغ")


# ===== استخدام الكلاسات =====
سيارتي = سيارة("تويوتا", "كامري", 2023, 4)
سيارتي.تشغيل_المحرك()
سيارتي.فتح_صندوق()
print(سيارتي)

شاحنتي = شاحنة("فورد", "F-150", 2022, 1000)
شاحنتي.تحميل_البضاعة(500)
شاحنتي.تحميل_البضاعة(1500)

# ===== التحقق من الوراثة =====
print(f"\nهل السيارة مركبة؟ {isinstance(سيارتي, مركبة)}")
print(f"هل الشاحنة سيارة؟ {isinstance(شاحنتي, سيارة)}")

# ===== تعدد الوراثة =====
class طائر:
    def يطير(self):
        print("أنا أطير!")

class سباح:
    def يسبح(self):
        print("أنا أسبح!")

class بطة(طائر, سباح):
    def يمشي(self):
        print("أنا أمشي أيضاً!")

بطتي = بطة()
بطتي.يطير()
بطتي.يسبح()
بطتي.يمشي()

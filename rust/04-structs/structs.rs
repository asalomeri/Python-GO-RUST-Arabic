// الهياكل في Rust - Structs
#[derive(Debug)]
struct نقطة {
    x: f64,
    y: f64,
}

impl نقطة {
    // دالة إنشاء (constructor)
    fn جديد(x: f64, y: f64) -> Self {
        نقطة { x, y }
    }

    // حساب المسافة من الأصل
    fn مسافة_من_الأصل(&self) -> f64 {
        (self.x * self.x + self.y * self.y).sqrt()
    }

    // حساب المسافة بين نقطتين
    fn مسافة_من(&self, أخرى: &نقطة) -> f64 {
        let dx = self.x - أخرى.x;
        let dy = self.y - أخرى.y;
        (dx * dx + dy * dy).sqrt()
    }
}

#[derive(Debug)]
struct مستطيل {
    طول: f64,
    عرض: f64,
}

impl مستطيل {
    fn جديد(طول: f64, عرض: f64) -> Self {
        مستطيل { طول, عرض }
    }

    fn مساحة(&self) -> f64 {
        self.طول * self.عرض
    }

    fn محيط(&self) -> f64 {
        2.0 * (self.طول + self.عرض)
    }

    fn هل_مربع(&self) -> bool {
        self.طول == self.عرض
    }
}

// ===== Enums =====
#[derive(Debug)]
enum شكل {
    دائرة(f64),
    مستطيل_شكل(f64, f64),
    مثلث(f64, f64, f64),
}

impl شكل {
    fn مساحة(&self) -> f64 {
        match self {
            شكل::دائرة(نصف_قطر) => std::f64::consts::PI * نصف_قطر * نصف_قطر,
            شكل::مستطيل_شكل(طول, عرض) => طول * عرض,
            شكل::مثلث(أ, ب, ج) => {
                let s = (أ + ب + ج) / 2.0;
                (s * (s - أ) * (s - ب) * (s - ج)).sqrt()
            }
        }
    }
}

fn main() {
    // ===== Structs =====
    println!("=== النقاط ===");
    let نقطة1 = نقطة::جديد(3.0, 4.0);
    println!("النقطة: {:?}", نقطة1);
    println!("المسافة من الأصل: {:.2}", نقطة1.مسافة_من_الأصل());

    let نقطة2 = نقطة::جديد(0.0, 0.0);
    println!("المسافة بين النقطتين: {:.2}", نقطة1.مسافة_من(&نقطة2));

    println!("\n=== المستطيل ===");
    let مربع = مستطيل::جديد(5.0, 5.0);
    println!("المساحة: {:.2}", مربع.مساحة());
    println!("المحيط: {:.2}", مربع.محيط());
    println!("هل مربع؟ {}", مربع.هل_مربع());

    // ===== Enums =====
    println!("\n=== الأشكال ===");
    let أشكال = vec![
        شكل::دائرة(5.0),
        شكل::مستطيل_شكل(4.0, 6.0),
        شكل::مثلث(3.0, 4.0, 5.0),
    ];

    for شكل_محدد in &أشكال {
        println!("{:?} - المساحة: {:.2}", شكل_محدد, شكل_محدد.مساحة());
    }
}

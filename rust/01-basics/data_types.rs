// أنواع البيانات في Rust - Data Types
fn main() {
    // ===== الأعداد الصحيحة =====
    println!("=== الأعداد الصحيحة ===");
    let i8_val: i8 = 127;
    let i16_val: i16 = 32767;
    let i32_val: i32 = 2147483647;
    let i64_val: i64 = 9223372036854775807;
    let u8_val: u8 = 255;
    let u32_val: u32 = 4294967295;
    println!("i8: {}, i16: {}, i32: {}, i64: {}", i8_val, i16_val, i32_val, i64_val);
    println!("u8: {}, u32: {}", u8_val, u32_val);

    // ===== الأعداد العشرية =====
    println!("\n=== الأعداد العشرية ===");
    let f32_val: f32 = 3.14;
    let f64_val: f64 = 3.141592653589793;
    println!("f32: {:.2}, f64: {:.15}", f32_val, f64_val);

    // ===== القيم المنطقية =====
    println!("\n=== القيم المنطقية ===");
    let صحيح: bool = true;
    let خاطئ: bool = false;
    println!("AND: {}, OR: {}, NOT: {}", صحيح && خاطئ, صحيح || خاطئ, !صحيح);

    // ===== الأحرف والنصوص =====
    println!("\n=== الأحرف والنصوص ===");
    let حرف = 'أ';
    let نص: &str = "مرحبا بالعالم";
    let نص_مملوك: String = String::from("مرحبا");
    println!("حرف: {}", حرف);
    println!("نص: {}", نص);
    println!("نص مملوك: {}", نص_مملوك);
    println!("طول النص: {}", نص.chars().count());

    // ===== الصفوف (Tuples) =====
    println!("\n=== الصفوف ===");
    let صفة: (i32, f64, &str) = (42, 3.14, "مرحبا");
    println!("العدد: {}, العشري: {}, النص: {}", صفة.0, صفة.1, صفة.2);

    let (أ, ب, ج) = صفة;  // تفكيك الصفة
    println!("أ={}, ب={}, ج={}", أ, ب, ج);

    // ===== المصفوفات (Arrays) =====
    println!("\n=== المصفوفات ===");
    let أعداد: [i32; 5] = [1, 2, 3, 4, 5];
    println!("المصفوفة: {:?}", أعداد);
    println!("العنصر الأول: {}", أعداد[0]);
    println!("الطول: {}", أعداد.len());

    let أصفار = [0; 5];  // 5 أصفار
    println!("أصفار: {:?}", أصفار);
}

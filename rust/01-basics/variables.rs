// المتغيرات في Rust - Variables
fn main() {
    // المتغيرات غير قابلة للتغيير افتراضياً
    let x = 5;
    println!("x = {}", x);

    // المتغيرات القابلة للتغيير
    let mut y = 10;
    println!("y قبل التغيير = {}", y);
    y = 20;
    println!("y بعد التغيير = {}", y);

    // تحديد النوع صراحةً
    let عدد_صحيح: i32 = 42;
    let عدد_عشري: f64 = 3.14;
    let حرف: char = 'أ';
    let منطقي: bool = true;

    println!("عدد صحيح: {}", عدد_صحيح);
    println!("عدد عشري: {}", عدد_عشري);
    println!("حرف: {}", حرف);
    println!("منطقي: {}", منطقي);

    // الثوابت
    const الحد_الأقصى: u32 = 100_000;
    const PI: f64 = 3.141592653589793;
    println!("الحد الأقصى: {}", الحد_الأقصى);
    println!("π = {}", PI);

    // shadowing
    let z = 5;
    let z = z + 1;    // متغير جديد يخفي القديم
    let z = z * 2;
    println!("z = {}", z);  // 12

    // shadowing بتغيير النوع
    let مساحة = "مربع";
    let مساحة = مساحة.len();  // الآن هو عدد صحيح
    println!("طول كلمة 'مربع': {}", مساحة);
}

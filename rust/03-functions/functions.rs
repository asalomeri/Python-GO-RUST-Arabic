// الدوال في Rust - Functions
fn main() {
    // ===== دالة بسيطة =====
    مرحبا();

    // ===== دالة بمعاملات =====
    تحية("أحمد");

    // ===== دالة بعودة =====
    let نتيجة = جمع(5, 3);
    println!("5 + 3 = {}", نتيجة);

    // ===== دالة بتعبيرات =====
    let مربع = {
        let x = 4;
        x * x  // تعبير (بدون ;)
    };
    println!("مربع 4 = {}", مربع);

    // ===== الإغلاقات (Closures) =====
    println!("\n=== الإغلاقات ===");
    let ضعف = |x| x * 2;
    println!("ضعف 5 = {}", ضعف(5));

    let جمع_ثلاثة = |x, y, z| x + y + z;
    println!("1+2+3 = {}", جمع_ثلاثة(1, 2, 3));

    // ===== map و filter =====
    let أعداد = vec![1, 2, 3, 4, 5];
    let مربعات: Vec<i32> = أعداد.iter().map(|&x| x * x).collect();
    println!("المربعات: {:?}", مربعات);

    let زوجية: Vec<&i32> = أعداد.iter().filter(|&&x| x % 2 == 0).collect();
    println!("الزوجية: {:?}", زوجية);

    // ===== فيبوناتشي =====
    println!("\n=== فيبوناتشي ===");
    for i in 0..10 {
        print!("{} ", فيبوناتشي(i));
    }
    println!();
}

fn مرحبا() {
    println!("مرحبا بالعالم!");
}

fn تحية(اسم: &str) {
    println!("مرحبا يا {}!", اسم);
}

fn جمع(أ: i32, ب: i32) -> i32 {
    أ + ب  // بدون return وبدون ;
}

fn فيبوناتشي(n: u32) -> u32 {
    match n {
        0 => 0,
        1 => 1,
        _ => فيبوناتشي(n - 1) + فيبوناتشي(n - 2),
    }
}

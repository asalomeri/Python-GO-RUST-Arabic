// لعبة بسيطة - Simple Game في Rust
use std::io::{self, BufRead, Write};

#[derive(Debug, PartialEq)]
enum نتيجة_الدور {
    فوز,
    خسارة,
    تعادل,
    خروج,
}

#[derive(Debug, Clone, PartialEq)]
enum اختيار {
    حجر,
    ورقة,
    مقص,
}

fn تحويل_إلى_اختيار(نص: &str) -> Option<اختيار> {
    match نص.trim() {
        "1" | "حجر" => Some(اختيار::حجر),
        "2" | "ورقة" => Some(اختيار::ورقة),
        "3" | "مقص" => Some(اختيار::مقص),
        _ => None,
    }
}

fn اختيار_الحاسوب(دور: u64) -> اختيار {
    match دور % 3 {
        0 => اختيار::حجر,
        1 => اختيار::ورقة,
        _ => اختيار::مقص,
    }
}

fn تحديد_الفائز(لاعب: &اختيار, حاسوب: &اختيار) -> نتيجة_الدور {
    if لاعب == حاسوب {
        return نتيجة_الدور::تعادل;
    }

    match (لاعب, حاسوب) {
        (اختيار::حجر, اختيار::مقص) => نتيجة_الدور::فوز,
        (اختيار::ورقة, اختيار::حجر) => نتيجة_الدور::فوز,
        (اختيار::مقص, اختيار::ورقة) => نتيجة_الدور::فوز,
        _ => نتيجة_الدور::خسارة,
    }
}

fn main() {
    println!("🎮 لعبة حجر ورقة مقص!");
    println!("========================\n");

    let mut انتصارات = 0u32;
    let mut خسائر = 0u32;
    let mut تعادلات = 0u32;
    let mut دور = 0u64;

    let stdin = io::stdin();
    let mut أسطر = stdin.lock().lines();

    loop {
        println!("\nاختر:");
        println!("  1 - حجر 🪨");
        println!("  2 - ورقة 📄");
        println!("  3 - مقص ✂️");
        println!("  0 - خروج");
        print!("اختيارك: ");
        io::stdout().flush().unwrap();

        let سطر = match أسطر.next() {
            Some(Ok(s)) => s,
            _ => break,
        };

        if سطر.trim() == "0" {
            break;
        }

        let اختيار_اللاعب = match تحويل_إلى_اختيار(&سطر) {
            Some(a) => a,
            None => {
                println!("❌ اختيار غير صحيح!");
                continue;
            }
        };

        دور += 1;
        let اختيار_الحاسوب_val = اختيار_الحاسوب(دور);

        println!("\nاخترت: {:?}", اختيار_اللاعب);
        println!("الحاسوب اختار: {:?}", اختيار_الحاسوب_val);

        match تحديد_الفائز(&اختيار_اللاعب, &اختيار_الحاسوب_val) {
            نتيجة_الدور::فوز => {
                println!("🏆 فزت!");
                انتصارات += 1;
            }
            نتيجة_الدور::خسارة => {
                println!("😔 خسرت!");
                خسائر += 1;
            }
            نتيجة_الدور::تعادل => {
                println!("🤝 تعادل!");
                تعادلات += 1;
            }
            نتيجة_الدور::خروج => break,
        }

        println!("\nالنتيجة: ✅{} ❌{} 🤝{}", انتصارات, خسائر, تعادلات);
    }

    println!("\n=== النتيجة النهائية ===");
    println!("انتصارات: {}", انتصارات);
    println!("خسائر: {}", خسائر);
    println!("تعادلات: {}", تعادلات);
    println!("شكراً للعب! 👋");
}

// الملكية في Rust - Ownership
fn main() {
    // ===== الملكية الأساسية =====
    println!("=== الملكية الأساسية ===");
    let s1 = String::from("مرحبا");
    let s2 = s1;  // s1 تُنقل إلى s2 (moved)
    // println!("{}", s1);  // خطأ! s1 لم تعد صالحة
    println!("s2 = {}", s2);

    // ===== النسخ =====
    println!("\n=== النسخ ===");
    let x = 5;
    let y = x;  // الأعداد الصحيحة تُنسخ (Copy)
    println!("x = {}, y = {}", x, y);  // كلاهما صالح

    let s3 = String::from("سلام");
    let s4 = s3.clone();  // نسخ صريح
    println!("s3 = {}, s4 = {}", s3, s4);

    // ===== الملكية مع الدوال =====
    println!("\n=== الملكية مع الدوال ===");
    let نص = String::from("مرحبا بالعالم");
    طباعة_نص(نص);
    // println!("{}", نص);  // خطأ! نقلت الملكية للدالة

    let عدد = 42;
    طباعة_عدد(عدد);
    println!("العدد لا يزال: {}", عدد);  // صالح لأن i32 يُنسخ

    // ===== إعادة الملكية =====
    println!("\n=== إعادة الملكية ===");
    let s5 = String::from("نص مهم");
    let s6 = أخذ_وإعادة(s5);
    println!("s6 = {}", s6);
}

fn طباعة_نص(نص: String) {
    println!("الدالة تطبع: {}", نص);
    // الملكية تُحرر هنا
}

fn طباعة_عدد(عدد: i32) {
    println!("الدالة تطبع: {}", عدد);
}

fn أخذ_وإعادة(نص: String) -> String {
    println!("أخذت الملكية ثم أعدتها");
    نص  // إعادة الملكية للمستدعي
}

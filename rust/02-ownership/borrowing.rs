// الاستعارة في Rust - Borrowing
fn main() {
    // ===== الاستعارة غير القابلة للتغيير =====
    println!("=== الاستعارة غير القابلة للتغيير ===");
    let s1 = String::from("مرحبا");
    let طول = حساب_الطول(&s1);  // استعارة مرجع
    println!("طول '{}' هو {}", s1, طول);  // s1 لا تزال صالحة

    // ===== استعارات متعددة =====
    println!("\n=== استعارات متعددة ===");
    let s2 = String::from("عالم");
    let r1 = &s2;
    let r2 = &s2;
    println!("r1 = {}, r2 = {}", r1, r2);  // كلاهما يقرأان s2

    // ===== الاستعارة القابلة للتغيير =====
    println!("\n=== الاستعارة القابلة للتغيير ===");
    let mut s3 = String::from("مرحبا");
    تعديل_نص(&mut s3);
    println!("بعد التعديل: {}", s3);

    // ===== شرائح النص (String Slices) =====
    println!("\n=== شرائح النص ===");
    let جملة = String::from("مرحبا يا عالم");
    let أول_كلمة = أول_كلمة(&جملة);
    println!("أول كلمة: {}", أول_كلمة);

    // ===== شرائح المصفوفات =====
    println!("\n=== شرائح المصفوفات ===");
    let أعداد = [1, 2, 3, 4, 5];
    let شريحة = &أعداد[1..3];
    println!("الشريحة: {:?}", شريحة);
}

fn حساب_الطول(نص: &String) -> usize {
    نص.chars().count()
}

fn تعديل_نص(نص: &mut String) {
    نص.push_str("، كيف حالك؟");
}

fn أول_كلمة(نص: &str) -> &str {
    let bytes = نص.as_bytes();
    for (i, &بايت) in bytes.iter().enumerate() {
        if بايت == b' ' {
            return &نص[0..i];
        }
    }
    &نص[..]
}
